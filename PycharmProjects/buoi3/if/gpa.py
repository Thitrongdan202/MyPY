gpa = float (input("Nhập điểm trung bình"))
if gpa >= 9:
    print ("Học lực xuất sắc")
elif gpa >=8:
    print ("Học lực giỏi")
elif gpa >= 7:
    print ("Học lực khá")
elif gpa >= 5:
    print ("Học lực trung bình")
elif gpa >= 3.5:
    print ("Học lực yếu")
else:
     print ("Học lực kém")